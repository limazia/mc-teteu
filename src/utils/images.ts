// Imagem especial para o Natal (25 de dezembro)
const CHRISTMAS_IMAGE = "happy-christmas.png";

// Imagem para o período pós-Natal (26 de dezembro até 1 de janeiro)
const POST_CHRISTMAS_IMAGE = "teteu.png";

// Função para obter a imagem do dia
export function getImageForDate(date: Date): string {
  const month = date.getMonth(); // 0-11
  const day = date.getDate();

  // Caso especial: 25 de dezembro (Natal)
  if (month === 11 && day === 25) {
    return CHRISTMAS_IMAGE;
  }

  // Caso especial: período pós-Natal (26-31 de dezembro)
  if (month === 11 && day > 25) {
    return POST_CHRISTMAS_IMAGE;
  }

  // Para os outros dias do ano
  const start = new Date(date.getFullYear(), 0, 1);
  const today = new Date(date.getFullYear(), month, day);
  const dayOfYear =
    Math.floor((today.getTime() - start.getTime()) / (24 * 60 * 60 * 1000)) + 1;

  // Garante que o dia está dentro do intervalo válido (1-358)
  if (dayOfYear >= 1 && dayOfYear <= 358) {
    return imageMapping[dayOfYear];
  }

  // Caso padrão: retorna a primeira imagem
  return imageMapping[1];
}
